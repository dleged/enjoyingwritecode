
// "Number of Bits"问题要求计算给定整数的二进制表示中有多少个位为1。这可以通过位操作来实现，具体步骤如下：

// 初始化一个计数器变量，用于记录位为1的数量。
// 使用位运算的技巧，不断将整数右移1位，并检查最低位是否为1。
// 如果最低位为1，则将计数器加1。
// 继续右移整数，直到整数为0。
// 返回计数器的值，即为二进制表示中位为1的数量。

function countBits(num) {
  let count = 0;
  while (num !== 0) {
      count += num & 1; // 检查最低位是否为1
      num = num >>> 1; // 右移一位
  }
  return count;
}

